num1=int(input("enter first number:- "))
num2=int(input("enter second number:- "))
Operator=input("enter any operator:- ")

if Operator=="+":
    print(num1+num2)
if Operator=="-":
    print(num1-num2)
if Operator=="*":
    print(num1*num2)
if Operator=="/":
    print(num1/num2)
if Operator=="%":
    print(num1%num2)        