'''
---3.count()---
it is used to count the number of times an element appears in the list.
'''

l=[99,98,99,97,100,99,97,100,99,99]
print(l.count(99))
print(l.count(97))
print(l.count(100))
print(l.count(98))
print(l.count(999))

'''NOTE: If the element is not appears in the list then it will return 0.'''